    def _analyze_ml_services(self, repo_structure, previous_context, deps_data):
        # Check Terraform for ML-related resources
        tf_data = self._get_terraform_data()
        ml_resources = [r for r in tf_data.get("resources", [])
                       if any(kw in r["type"] for kw in ("sagemaker", "bedrock", "comprehend", "rekognition", "textract"))]
        # Also check for LLM/ML SDK imports from prompt security scanner
        prompt_data = self._get_prompt_security_data()
        llm_sdks = prompt_data.get("llm_sdks", [])

        if not ml_resources and not llm_sdks:
            return ("*No ML/AI service resources or SDK imports detected.*\n\n"
                    "---\n*Generated by static analysis (ENABLE_AI=false)*")

        lines = []

        if ml_resources:
            lines.append("**ML/AI Service Resources (Terraform):**\n")
            lines.append("| Resource Type | Name | File |")
            lines.append("|--------------|------|------|")
            for r in sorted(ml_resources, key=lambda x: x["type"]):
                lines.append(f"| `{r['type']}` | {r['name']} | {r['file']} |")
            lines.append("")

        if llm_sdks:
            lines.append("**LLM/AI SDK Usage Detected:**\n")
            lines.append("| SDK/Provider | File |")
            lines.append("|-------------|------|")
            for s in sorted(llm_sdks, key=lambda x: x["sdk"]):
                lines.append(f"| {s['sdk']} | `{s['file']}` |")

        lines.append("\n---")
        lines.append("*Generated by static analysis (ENABLE_AI=false)*")
        return "\n".join(lines)

