    def _analyze_authentication(self, repo_structure, previous_context, deps_data):
        data = self._get_security_data()
        scanner = SecurityScanner(self.repo_path)
        # Also merge Terraform IAM resources
        tf_data = self._get_terraform_data()
        iam_resources = [r for r in tf_data.get("resources", [])
                        if "iam" in r["type"].lower() or "cognito" in r["type"].lower()]
        md = scanner.format_authentication(data)
        if iam_resources and "No authentication" in md:
            # Fall back to Terraform IAM view
            lines = ["**Authentication/IAM Resources (Terraform):**\n"]
            lines.append("| Resource Type | Name | File |")
            lines.append("|--------------|------|------|")
            for r in sorted(iam_resources, key=lambda x: x["type"]):
                lines.append(f"| `{r['type']}` | {r['name']} | {r['file']} |")
            lines.append("\n---")
            lines.append("*Generated by static analysis (ENABLE_AI=false)*")
            return "\n".join(lines)
        if iam_resources and "No authentication" not in md:
            # Append Terraform IAM section
            lines = [md.rsplit("---", 1)[0].rstrip()]
            lines.append("\n**Terraform IAM Resources:**\n")
            lines.append("| Resource Type | Name | File |")
            lines.append("|--------------|------|------|")
            for r in sorted(iam_resources, key=lambda x: x["type"]):
                lines.append(f"| `{r['type']}` | {r['name']} | {r['file']} |")
            lines.append("\n---")
            lines.append("*Generated by static analysis (ENABLE_AI=false)*")
            return "\n".join(lines)
        return md

    def _analyze_authorization(self, repo_structure, previous_context, deps_data):
        data = self._get_security_data()
        scanner = SecurityScanner(self.repo_path)
        # Also merge Terraform authz resources
        tf_data = self._get_terraform_data()
        auth_resources = [r for r in tf_data.get("resources", [])
                         if any(kw in r["type"].lower() for kw in
                                ("security_group", "iam_policy", "iam_role_policy", "waf", "acl"))]
        md = scanner.format_authorization(data)
        if auth_resources and "No authorization" in md:
            lines = ["**Authorization/Access Control Resources (Terraform):**\n"]
            lines.append("| Resource Type | Name | Category | File |")
            lines.append("|--------------|------|----------|------|")
            for r in sorted(auth_resources, key=lambda x: x["type"]):
                lines.append(f"| `{r['type']}` | {r['name']} | {r['category']} | {r['file']} |")
            lines.append("\n---")
            lines.append("*Generated by static analysis (ENABLE_AI=false)*")
            return "\n".join(lines)
        if auth_resources and "No authorization" not in md:
            lines = [md.rsplit("---", 1)[0].rstrip()]
            lines.append("\n**Terraform Access Control Resources:**\n")
            lines.append("| Resource Type | Name | Category | File |")
            lines.append("|--------------|------|----------|------|")
            for r in sorted(auth_resources, key=lambda x: x["type"]):
                lines.append(f"| `{r['type']}` | {r['name']} | {r['category']} | {r['file']} |")
            lines.append("\n---")
            lines.append("*Generated by static analysis (ENABLE_AI=false)*")
            return "\n".join(lines)
        return md

