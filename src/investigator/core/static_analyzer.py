from .static_analyzers.event_detector import EventDetector
from .static_analyzers.prompt_security_scanner import PromptSecurityScanner


class StaticAnalyzer:
    """Produces structured markdown analysis from code scanning -- no AI required."""

    def __init__(self, repo_path: str, logger=None):
        """
        Args:
            repo_path: Absolute path to the repository root directory.
            logger: Optional logger instance.
        """
        self.repo_path = repo_path
        self.logger = logger or logging.getLogger(__name__)
        self._language_detector = LanguageDetector(logger=self.logger)

        # Cached detection results (populated on first use)
        self._lang_result: Optional[dict] = None
        self._terraform_data: Optional[dict] = None
        self._deployment_data: Optional[dict] = None
        self._api_data: Optional[dict] = None
        self._entity_data: Optional[dict] = None
        self._database_data: Optional[dict] = None
        self._monitoring_data: Optional[dict] = None
        self._feature_flag_data: Optional[dict] = None
        self._data_mapping_data: Optional[dict] = None
        self._security_data: Optional[dict] = None
        self._event_data: Optional[dict] = None
        self._prompt_security_data: Optional[dict] = None

        # Dispatch table: step_name -> analyzer method
        self._section_analyzers = {
            "hl_overview": self._analyze_hl_overview,
            "module_deep_dive": self._analyze_module_deep_dive,
            "dependencies": self._analyze_dependencies,
            "core_entities": self._analyze_core_entities,
            "DBs": self._analyze_databases,
            "APIs": self._analyze_apis,
            "events": self._analyze_events,
            "service_dependencies": self._analyze_service_dependencies,
            "deployment": self._analyze_deployment,
            "authentication": self._analyze_authentication,
            "authorization": self._analyze_authorization,
            "data_mapping": self._analyze_data_mapping,
            "security_check": self._analyze_security_check,
            "monitoring": self._analyze_monitoring,
            "ml_services": self._analyze_ml_services,
            "feature_flags": self._analyze_feature_flags,
            "prompt_security_check": self._analyze_prompt_security_check,
            "resources": self._analyze_resources,
            "environments": self._analyze_environments,
        }

    def analyze_section(
        self,
        step_name: str,
        repo_structure: str,
        previous_context: Optional[str] = None,
        deps_data: Optional[dict] = None,
    ) -> str:
        """
        Run static analysis for a specific section.

        Args:
            step_name: Name of the analysis step (one of the 17 sections).
            repo_structure: Repository file tree string (from RepositoryAnalyzer).
            previous_context: Previous analysis sections (for context-dependent steps).
            deps_data: Parsed dependency data from read_dependencies_activity.

        Returns:
            Markdown string in the same format Claude would produce.
        """
        analyzer_fn = self._section_analyzers.get(step_name)
        if analyzer_fn is None:
            self.logger.warning(f"No static analyzer for section: {step_name}")
            return self._placeholder_section(step_name)

        try:
            result = analyzer_fn(repo_structure, previous_context, deps_data)
            self.logger.info(
                f"Static analysis for '{step_name}': {len(result)} chars"
            )
            return result
        except Exception as e:
            self.logger.error(f"Static analysis failed for '{step_name}': {e}")
            return self._error_section(step_name, str(e))

    @property
    def lang_result(self) -> dict:
        """Lazily compute and cache language detection results."""
        if self._lang_result is None:
            self._lang_result = self._language_detector.detect(self.repo_path)
        return self._lang_result

    # -------------------------------------------------------------------------
    # Section analyzers
    # -------------------------------------------------------------------------

    def _analyze_hl_overview(
        self, repo_structure: str, previous_context: str, deps_data: dict
    ) -> str:
        """High-level overview: languages, LOC, tech stack, directory structure."""
        result = self.lang_result
        repo_name = Path(self.repo_path).name

        # Try to read README for project purpose
        purpose = self._read_readme_summary()

        lines = []
        lines.append(f"**Repository Name:** {repo_name}")
        lines.append("")
        if purpose:
            lines.append(f"**Project Purpose:** {purpose}")
            lines.append("")

        # Language & tech stack from LanguageDetector
        lines.append(result and self._language_detector.format_as_markdown(result) or "")

        # Directory structure (already provided as repo_structure)
        lines.append("**Repository Structure:**")
        lines.append("")
        lines.append("```")
        # Truncate if very long
        struct_lines = repo_structure.split("\n")
        if len(struct_lines) > 80:
            lines.extend(struct_lines[:80])
            lines.append(f"... ({len(struct_lines) - 80} more lines)")
        else:
            lines.append(repo_structure)
        lines.append("```")
        lines.append("")

        lines.append("---")
        lines.append("*Generated by static analysis (ENABLE_AI=false)*")

        return "\n".join(lines)

    def _analyze_module_deep_dive(
        self, repo_structure: str, previous_context: str, deps_data: dict
    ) -> str:
        """Module/package structure analysis from directory tree."""
        repo = Path(self.repo_path)
        modules = []

        # Walk top-level directories as "modules"
        for item in sorted(repo.iterdir()):
            if not item.is_dir():
                continue
            if item.name.startswith(".") or item.name in LanguageDetector.SKIP_DIRS:
                continue

            file_count = sum(1 for _ in item.rglob("*") if _.is_file())
            dir_count = sum(1 for _ in item.rglob("*") if _.is_dir())

            # Detect sub-packages
            sub_packages = []
            for sub in sorted(item.iterdir()):
                if sub.is_dir() and not sub.name.startswith(".") and sub.name not in LanguageDetector.SKIP_DIRS:
                    sub_files = sum(1 for _ in sub.rglob("*") if _.is_file())
                    sub_packages.append(f"  - `{sub.name}/` ({sub_files} files)")

            modules.append({
                "name": item.name,
                "files": file_count,
                "dirs": dir_count,
                "sub_packages": sub_packages,
            })

        lines = []
        lines.append("**Module/Directory Breakdown:**")
        lines.append("")

        if not modules:
            lines.append("*No significant module structure detected.*")
        else:
            lines.append("| Module | Files | Subdirectories |")
            lines.append("|--------|------:|---------------:|")
            for mod in modules:
                lines.append(f"| `{mod['name']}/` | {mod['files']:,} | {mod['dirs']:,} |")
            lines.append("")

            # Detail sub-packages
            for mod in modules:
                if mod["sub_packages"]:
                    lines.append(f"**`{mod['name']}/`** sub-packages:")
                    lines.extend(mod["sub_packages"])
                    lines.append("")

        lines.append("---")
        lines.append("*Generated by static analysis (ENABLE_AI=false)*")

        return "\n".join(lines)

    def _analyze_dependencies(
        self, repo_structure: str, previous_context: str, deps_data: dict
    ) -> str:
        """Format dependency data. Reuses read_dependencies_activity output if available."""
        if deps_data:
            # deps_data is already formatted markdown from the existing activity
            return deps_data if isinstance(deps_data, str) else str(deps_data)

        # Fallback: just list dependency files found
        repo = Path(self.repo_path)
        dep_files = []
        patterns = [
            "pom.xml", "build.gradle", "build.gradle.kts",
            "package.json", "go.mod", "Cargo.toml", "Gemfile",
            "requirements*.txt", "pyproject.toml", "Pipfile",
            "composer.json", "*.csproj",
        ]

        for pattern in patterns:
            for match in repo.rglob(pattern):
                rel = match.relative_to(repo)
                dep_files.append(str(rel))

        lines = []
        if dep_files:
            lines.append("**Dependency Files Found:**")
            lines.append("")
            for f in sorted(dep_files):
                lines.append(f"- `{f}`")
        else:
            lines.append("*No standard dependency files detected.*")

        lines.append("")
        lines.append("---")
        lines.append("*Generated by static analysis (ENABLE_AI=false)*")

        return "\n".join(lines)

