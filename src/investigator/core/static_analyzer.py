"""
Static Analyzer -- no-AI replacement for ClaudeAnalyzer.

When ENABLE_AI=false, this class replaces Claude API calls with automated
static code analysis. It produces the same .arch.md output format (17 H1
sections) so that the downstream pipeline (cross-repo diagrams, Architecture
Hub, caching) works identically.

Each section is generated by scanning the repository's files using
language-specific parsers, regex patterns, and config file parsing.
"""

import logging
import os
