    # ---- Helpers ----

    def _read_readme_summary(self) -> str:
        """Read first paragraph of README for project purpose."""
        repo = Path(self.repo_path)
        for readme_name in ["README.md", "README.rst", "README.txt", "README"]:
            readme = repo / readme_name
            if readme.is_file():
                try:
                    content = readme.read_text(encoding="utf-8", errors="ignore")
                    # Extract first non-heading, non-blank paragraph
                    for line in content.split("\n"):
                        stripped = line.strip()
                        if not stripped:
                            continue
                        if stripped.startswith("#") or stripped.startswith("="):
                            continue
                        if stripped.startswith("![") or stripped.startswith("[!"):
                            continue  # badges
                        # Return first real text line (truncated)
                        if len(stripped) > 300:
                            return stripped[:300] + "..."
                        return stripped
                except (OSError, UnicodeDecodeError):
                    pass
        return ""

    def _placeholder_section(self, step_name: str) -> str:
        """Return a placeholder for sections not yet implemented."""
        return (
            f"*Static analysis for `{step_name}` is not yet implemented. "
            f"This section will be populated in a future update.*\n\n"
            f"---\n"
            f"*Generated by static analysis (ENABLE_AI=false)*"
        )

    def _error_section(self, step_name: str, error: str) -> str:
        """Return an error section when analysis fails."""
        return (
            f"*Static analysis for `{step_name}` encountered an error: {error}*\n\n"
            f"---\n"
            f"*Generated by static analysis (ENABLE_AI=false)*"
        )
