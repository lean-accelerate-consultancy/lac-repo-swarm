    def _extract_aws_service_nodes(self) -> list[dict]:
        """Extract distinct AWS service nodes from resource categories and VPC endpoints."""
        seen_ids = set()
        services = []

        # From resource categories across all repos
        for repo in self._parsed_repos:
            for cat in repo["resource_categories"]:
                if cat in _SERVICE_MAP:
                    svc = _SERVICE_MAP[cat]
                    if svc["id"] not in seen_ids:
                        seen_ids.add(svc["id"])
                        services.append(dict(svc))

            # From VPC endpoint names (aws_vpc_endpoint resources)
            for res in repo["terraform_resources"]:
                if res["type"] == "aws_vpc_endpoint":
                    ep_name = res["name"].lower()
                    for key, svc in _VPC_ENDPOINT_SERVICE_MAP.items():
                        if key in ep_name and svc["id"] not in seen_ids:
                            seen_ids.add(svc["id"])
                            services.append(dict(svc))

            # From CloudFormation resources
            for res in repo["terraform_resources"]:
                if "cloudformation" in res["type"]:
                    svc_id = "svc_CF"
                    if svc_id not in seen_ids:
                        seen_ids.add(svc_id)
                        services.append({
                            "id": svc_id,
                            "display": "CloudFormation\nStack Management",
                            "category": "compute",
                        })

        return services

    def _map_repo_to_services(self, repo: dict) -> list[tuple[str, str]]:
        """Map a repo to the AWS service IDs it provisions.

        Returns list of (service_id, edge_label) tuples.
        """
        edges = []
        seen = set()

        # Map from resource categories
        cat_to_label = {
            "Networking": ("svc_VPC", "provisions network"),
            "Compute/EMR": ("svc_EMR", "deploys cluster"),
            "Compute/EC2": ("svc_EC2", "deploys"),
            "Compute/Lambda": ("svc_Lambda", "deploys"),
            "Compute/ECS": ("svc_ECS", "deploys"),
            "IAM": ("svc_IAM", "configures"),
            "Security": ("svc_SG", "configures"),
            "Load Balancing": ("svc_ELB", "provisions"),
            "Storage/S3": ("svc_S3", "references"),
            "Storage/EBS": ("svc_EBS", "provisions"),
            "Database/RDS": ("svc_RDS", "provisions"),
            "Database/DynamoDB": ("svc_DynamoDB", "provisions"),
            "Monitoring": ("svc_CW", "configures"),
        }
        for cat in repo["resource_categories"]:
            if cat in cat_to_label:
                svc_id, label = cat_to_label[cat]
                if svc_id not in seen:
                    seen.add(svc_id)
                    edges.append((svc_id, label))

        # Infer from VPC endpoint names
        for res in repo["terraform_resources"]:
            if res["type"] == "aws_vpc_endpoint":
                ep_name = res["name"].lower()
                for key, svc in _VPC_ENDPOINT_SERVICE_MAP.items():
                    if key in ep_name and svc["id"] not in seen:
                        seen.add(svc["id"])
                        edges.append((svc["id"], "references"))

        # CloudFormation resources
        for res in repo["terraform_resources"]:
            if "cloudformation" in res["type"] and "svc_CF" not in seen:
                seen.add("svc_CF")
                edges.append(("svc_CF", "orchestrates"))

        return edges

