    def _generate_module_diagrams(self) -> str:
        """Diagram 4: Per-repo module structure."""
        sections = []

        for i, repo in enumerate(self._parsed_repos):
            if not repo["modules"]:
                continue

            sections.append(f"### {repo['display_name']}")
            sections.append("")

            root_id = self._safe_id(repo["display_name"], i)
            title = f"Module Structure — {repo['display_name']}"
            lines = ["```mermaid", "---", f"title: {title}", "---", "graph TD"]
            lines.append(f'    {root_id}["{repo["display_name"]}"]')

            for j, mod in enumerate(repo["modules"]):
                mod_id = f"{root_id}_mod{j}"
                file_word = "file" if mod["files"] == 1 else "files"
                label = f"{mod['name']}/<br/>{mod['files']} {file_word}"
                lines.append(f'    {root_id} --> {mod_id}["{label}"]')

            lines.append("```")
            sections.append("\n".join(lines))
            sections.append("")

        return "\n".join(sections) if sections else ""

    def _generate_language_pie_chart(self) -> str:
        """Diagram 5: Aggregated language distribution pie chart."""
        agg: dict[str, int] = defaultdict(int)
        for repo in self._parsed_repos:
            for lang, loc in repo["languages"].items():
                agg[lang] += loc

        if not agg:
            return ""

        lines = ["```mermaid", 'pie title Language Distribution (LOC)']
        for lang in sorted(agg, key=lambda x: -agg[x]):
            lines.append(f'    "{lang}" : {agg[lang]}')
        lines.append("```")
        return "\n".join(lines)

