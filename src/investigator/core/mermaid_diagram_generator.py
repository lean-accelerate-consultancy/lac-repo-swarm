    def _extract_section(self, content: str, section_name: str) -> str | None:
        """Extract the content of a specific H1 section from .arch.md content."""
        pattern = re.compile(r'^# ' + re.escape(section_name) + r'\s*$', re.MULTILINE)
        match = pattern.search(content)
        if not match:
            return None

        start = match.end()
        next_h1 = re.search(r'^# \w', content[start:], re.MULTILINE)
        if next_h1:
            section_text = content[start:start + next_h1.start()]
        else:
            section_text = content[start:]

        return section_text.strip()

    def _safe_participant(self, name: str, index: int) -> tuple:
        """Return (safe_id, display_name) for a sequence diagram participant."""
        safe = re.sub(r'[^a-zA-Z0-9]', '_', name)
        return f"p{index}_{safe}", name

