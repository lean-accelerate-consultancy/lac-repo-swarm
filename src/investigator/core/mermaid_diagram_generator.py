    def _generate_sequence_diagrams(self) -> str:
        """Generate Mermaid sequence diagrams from API, event, DB, and IaC data."""
        flows = []

        # Flow 1: API Request Flow
        api_flow = self._build_api_sequence_flow()
        if api_flow:
            flows.append(api_flow)

        # Flow 2: Event-Driven Flow
        event_flow = self._build_event_sequence_flow()
        if event_flow:
            flows.append(event_flow)

        # Flow 3: Shared Database Flow
        db_flow = self._build_shared_db_flow()
        if db_flow:
            flows.append(db_flow)

        # IaC Provisioning Flow (always add for IaC repos, not just as fallback)
        iac_flow = self._build_iac_provisioning_flow()
        if iac_flow:
            # If no service flows exist, IaC is primary; otherwise add after
            if not flows:
                flows.append(iac_flow)
            else:
                flows.append(iac_flow)

        # CloudFormation Integration Flow (conditional)
        cf_flow = self._build_cf_integration_flow()
        if cf_flow:
            flows.append(cf_flow)

        # Runtime/Operational Flow (conditional — EMR job submission)
        runtime_flow = self._build_runtime_sequence_flow()
        if runtime_flow:
            flows.append(runtime_flow)

        if not flows:
            return ""

        # Limit to 4 flows max
        flows = flows[:4]

        parts = []
        for i, (title, description, mermaid_lines) in enumerate(flows, 1):
            parts.append(f"### Flow {i}: {title}")
            parts.append("")
            parts.append(f"**Description:** {description}")
            parts.append("")
            parts.append("```mermaid")
            parts.append("---")
            parts.append(f"title: {title}")
            parts.append("---")
            parts.append("sequenceDiagram")
            parts.append("    autonumber")
            for line in mermaid_lines:
                parts.append(f"    {line}")
            parts.append("```")
            parts.append("")

        return "\n".join(parts)

    def _build_api_sequence_flow(self) -> tuple | None:
        """Build a sequence diagram for API request/response flows."""
        api_repos = [
            (i, r) for i, r in enumerate(self._parsed_repos)
            if r["api_endpoints"]
        ]
        if not api_repos:
            return None

        lines = []
        participants = []

        participants.append("participant Client")

        for idx, repo in api_repos[:3]:
            pid, display = self._safe_participant(repo["display_name"], idx)
            participants.append(f'participant {pid} as {display}')

        db_repos = [(i, r) for i, r in api_repos if r["databases"]]
        db_name = None
        if db_repos:
            db_name = db_repos[0][1]["databases"][0]
            safe_db = re.sub(r'[^a-zA-Z0-9]', '_', db_name)
            participants.append(f"participant {safe_db} as {db_name}")

        lines.extend(participants)

        for idx, repo in api_repos[:3]:
            pid, _ = self._safe_participant(repo["display_name"], idx)
            endpoints = sorted(
                repo["api_endpoints"],
                key=lambda e: (
                    0 if e["method"] in ("POST", "PUT") else
                    1 if e["method"] == "DELETE" else 2
                ),
            )[:3]

            for ep in endpoints:
                lines.append(f"Client->>{pid}: {ep['method']} {ep['path']}")
                lines.append(f"activate {pid}")
                if db_name and idx == api_repos[0][0]:
                    safe_db = re.sub(r'[^a-zA-Z0-9]', '_', db_name)
                    lines.append(f"Note over {pid}: Validate request")
                    lines.append(f"{pid}->>{safe_db}: persist data")
                    lines.append(f"{safe_db}-->>{pid}: OK")
                lines.append(f"{pid}-->>Client: response")
                lines.append(f"deactivate {pid}")

        return (
            "API Request Flow",
            "Request flow showing API endpoints and database interactions across services.",
            lines,
        )

    def _build_event_sequence_flow(self) -> tuple | None:
        """Build a sequence diagram for event-driven message flows."""
        event_repos = [
            (i, r) for i, r in enumerate(self._parsed_repos)
            if r["event_brokers"] or r["event_topics"]
        ]
        if not event_repos:
            return None

        topic_to_repos: dict[str, list[int]] = defaultdict(list)
        for idx, repo in event_repos:
            for topic in repo["event_topics"]:
                topic_to_repos[topic["name"]].append(idx)

        shared_topics = {t: repos for t, repos in topic_to_repos.items() if len(repos) > 1}

        all_brokers = set()
        for _, repo in event_repos:
            all_brokers.update(repo["event_brokers"])
        broker_name = sorted(all_brokers)[0] if all_brokers else "MessageBroker"

        lines = []
        participants = []

        if shared_topics:
            involved_indices = set()
            for repos in shared_topics.values():
                involved_indices.update(repos)

            for idx in sorted(involved_indices)[:4]:
                repo = self._parsed_repos[idx]
                pid, display = self._safe_participant(repo["display_name"], idx)
                participants.append(f'participant {pid} as {display}')

            safe_broker = re.sub(r'[^a-zA-Z0-9]', '_', broker_name)
            participants.append(f"participant {safe_broker} as {broker_name}")

            db_name = None
            for idx in sorted(involved_indices):
                if self._parsed_repos[idx]["databases"]:
                    db_name = self._parsed_repos[idx]["databases"][0]
                    break
            if db_name:
                safe_db = re.sub(r'[^a-zA-Z0-9]', '_', db_name)
                participants.append(f"participant {safe_db} as {db_name}")

            lines.extend(participants)
            lines.append(f"Note over {safe_broker}: Async message delivery")

            for topic_name in list(shared_topics.keys())[:3]:
                repo_indices = shared_topics[topic_name]
                pub_idx = repo_indices[0]
                pub_pid, _ = self._safe_participant(
                    self._parsed_repos[pub_idx]["display_name"], pub_idx
                )
                lines.append(f"{pub_pid}->>{safe_broker}: publish {topic_name}")

                for cons_idx in repo_indices[1:]:
                    cons_pid, _ = self._safe_participant(
                        self._parsed_repos[cons_idx]["display_name"], cons_idx
                    )
                    lines.append(f"{safe_broker}-->>{cons_pid}: consume {topic_name}")
                    lines.append(f"activate {cons_pid}")
                    if db_name and self._parsed_repos[cons_idx]["databases"]:
                        safe_db = re.sub(r'[^a-zA-Z0-9]', '_', db_name)
                        lines.append(f"{cons_pid}->>{safe_db}: update data")
                    lines.append(f"deactivate {cons_pid}")
        else:
            idx, repo = event_repos[0]
            pid, display = self._safe_participant(repo["display_name"], idx)
            safe_broker = re.sub(r'[^a-zA-Z0-9]', '_', broker_name)

            participants.append(f'participant {pid} as {display}')
            participants.append(f"participant {safe_broker} as {broker_name}")

            db_name = None
            if repo["databases"]:
                db_name = repo["databases"][0]
                safe_db = re.sub(r'[^a-zA-Z0-9]', '_', db_name)
                participants.append(f"participant {safe_db} as {db_name}")

            lines.extend(participants)
            lines.append(f"Note over {safe_broker}: Async message delivery")

            for topic in repo["event_topics"][:3]:
                lines.append(f"{pid}->>{safe_broker}: publish {topic['name']}")
                lines.append(f"{safe_broker}-->>{pid}: consume {topic['name']}")
                if db_name:
                    safe_db = re.sub(r'[^a-zA-Z0-9]', '_', db_name)
                    lines.append(f"{pid}->>{safe_db}: update data")

        description = (
            f"Asynchronous event flow via {broker_name}"
            + (" across multiple services." if shared_topics else " within a single service.")
        )

        return (
            "Event-Driven Message Flow",
            description,
            lines,
        )

    def _build_shared_db_flow(self) -> tuple | None:
        """Build a sequence diagram when multiple repos share a database type."""
        db_to_repos: dict[str, list[int]] = defaultdict(list)
        for i, repo in enumerate(self._parsed_repos):
            for db in repo["databases"]:
                db_to_repos[db].append(i)

        shared_dbs = {db: repos for db, repos in db_to_repos.items() if len(repos) > 1}
        if not shared_dbs:
            return None

        event_repos_set = {
            i for i, r in enumerate(self._parsed_repos)
            if r["event_brokers"] or r["event_topics"]
        }
        all_db_repos = set()
        for repos in shared_dbs.values():
            all_db_repos.update(repos)
        if all_db_repos.issubset(event_repos_set):
            return None

        lines = []
        participants = []

        db_name = sorted(shared_dbs.keys())[0]
        repo_indices = shared_dbs[db_name][:4]

        for idx in repo_indices:
            repo = self._parsed_repos[idx]
            pid, display = self._safe_participant(repo["display_name"], idx)
            participants.append(f'participant {pid} as {display}')

        safe_db = re.sub(r'[^a-zA-Z0-9]', '_', db_name)
        participants.append(f"participant {safe_db} as {db_name}")

        lines.extend(participants)

        for j, idx in enumerate(repo_indices):
            pid, _ = self._safe_participant(
                self._parsed_repos[idx]["display_name"], idx
            )
            if j == 0:
                lines.append(f"{pid}->>{safe_db}: write data")
                lines.append(f"activate {safe_db}")
                lines.append(f"{safe_db}-->>{pid}: OK")
                lines.append(f"deactivate {safe_db}")
            else:
                lines.append(f"{pid}->>{safe_db}: read data")
                lines.append(f"activate {safe_db}")
                lines.append(f"{safe_db}-->>{pid}: result set")
                lines.append(f"deactivate {safe_db}")

        return (
            "Shared Data Store Access",
            f"Multiple services accessing shared {db_name} database.",
            lines,
        )

    def _build_iac_provisioning_flow(self) -> tuple | None:
        """Build a rich sequence diagram for Terraform module provisioning."""
        iac_repos = [
            (i, r) for i, r in enumerate(self._parsed_repos)
            if r["module_chain"]
        ]
        if not iac_repos:
            return None

        lines = []
        participants = []

        # Determine cloud API participants from resource categories
        all_categories = set()
        for _, repo in iac_repos:
            all_categories.update(repo["resource_categories"].keys())

        # Add Operator/CI and Terraform participants
        participants.append("participant Op as Operator/CI Pipeline")
        participants.append("participant TF as Terraform")

        # Add state backend
        has_state = True  # Terraform always has state
        if has_state:
            participants.append("participant State as State Backend")

        # Map resource categories to cloud API participants
        api_participants: list[tuple[str, str]] = []
        cat_to_api = {
            "Networking": ("VPC_API", "AWS VPC API"),
            "Compute/EMR": ("EMR_API", "AWS EMR API"),
            "Compute/EC2": ("EC2_API", "AWS EC2 API"),
            "IAM": ("IAM_API", "AWS IAM API"),
            "Load Balancing": ("LB_API", "AWS ELB API"),
            "Security": ("SG_API", "AWS Security API"),
        }
        seen_apis = set()
        for cat in sorted(all_categories):
            if cat in cat_to_api and cat_to_api[cat][0] not in seen_apis:
                pid, display = cat_to_api[cat]
                api_participants.append((pid, display))
                seen_apis.add(pid)
                participants.append(f"participant {pid} as {display}")

        lines.extend(participants)

        # Opening: terraform plan/apply
        lines.append("Op->>TF: terraform plan/apply")
        lines.append("activate TF")

        # State management
        lines.append("TF->>State: Lock state file")
        lines.append("TF->>State: Read current state")
        lines.append("Note over TF: Calculate resource diff")

        # For each repo, show module provisioning with cloud API interactions
        for idx, repo in iac_repos[:2]:
            if len(iac_repos) > 1:
                lines.append(f"Note over TF: Provisioning {repo['display_name']}")

            for mod in repo["module_chain"][:6]:
                mod_name = mod["name"]
                mod_source = mod["source"].lstrip("./")

                # Determine which API this module likely calls based on module name
                target_api = None
                if "vpc" in mod_name.lower() or "network" in mod_name.lower():
                    target_api = "VPC_API"
                elif "emr" in mod_name.lower():
                    target_api = "EMR_API"
                elif "ec2" in mod_name.lower() or "instance" in mod_name.lower() or "bastion" in mod_name.lower():
                    target_api = "EC2_API"
                elif "iam" in mod_name.lower():
                    target_api = "IAM_API"
                elif "lb" in mod_name.lower() or "load" in mod_name.lower():
                    target_api = "LB_API"
                elif "security" in mod_name.lower() or "sg" in mod_name.lower():
                    target_api = "SG_API"

                # Only use target_api if it was added as a participant
                if target_api and target_api not in seen_apis:
                    target_api = None

                if target_api:
                    lines.append(f"TF->>{target_api}: Create {mod_name} resources")
                    lines.append(f"activate {target_api}")

                    # Find outputs for this module
                    mod_outputs = [
                        o for o in repo["outputs"]
                        if mod_source in o["file"]
                    ]
                    if mod_outputs:
                        output_names = ", ".join(o["name"] for o in mod_outputs[:3])
                        if len(mod_outputs) > 3:
                            output_names += f" +{len(mod_outputs) - 3} more"
                        lines.append(f"{target_api}-->>TF: {output_names}")
                    else:
                        lines.append(f"{target_api}-->>TF: Resource IDs")

                    lines.append(f"deactivate {target_api}")
                else:
                    # Fallback to the old style for modules we can't map
                    lines.append(f"Note over TF: Provision {mod_name} ({mod_source})")
                    mod_outputs = [
                        o for o in repo["outputs"]
                        if mod_source in o["file"]
                    ]
                    if mod_outputs:
                        output_names = ", ".join(o["name"] for o in mod_outputs[:3])
                        if len(mod_outputs) > 3:
                            output_names += f" +{len(mod_outputs) - 3} more"
                        lines.append(f"Note over TF: Outputs: {output_names}")

        # Add polling for compute resources (EMR, EC2)
        if "EMR_API" in seen_apis:
            lines.append("loop Poll until cluster ready")
            lines.append("TF->>EMR_API: Describe Cluster Status")
            lines.append("EMR_API-->>TF: Status (STARTING/WAITING)")
            lines.append("end")
            lines.append("Note right of EMR_API: Cluster bootstrapping ~5-15 min")

        # Close out
        lines.append("TF->>State: Update state file")
        lines.append("TF->>State: Release lock")
        lines.append("TF-->>Op: Apply complete")
        lines.append("deactivate TF")

        return (
            "Infrastructure Provisioning Sequence",
            "Terraform module provisioning order showing infrastructure dependency chain.",
            lines,
        )

    def _build_cf_integration_flow(self) -> tuple | None:
        """Build a sequence diagram for Terraform-CloudFormation integration."""
        # Detect repos with CloudFormation resources
        cf_repos = []
        for i, repo in enumerate(self._parsed_repos):
            for res in repo["terraform_resources"]:
                if "cloudformation" in res["type"]:
                    cf_repos.append((i, repo))
                    break

        if not cf_repos:
            return None

        # Check if EMR exists globally
        has_emr = any(
            "Compute/EMR" in r["resource_categories"]
            for r in self._parsed_repos
        )

        lines = []
        participants = [
            "participant Op as Operator",
            "participant TF as Terraform",
            "participant CF as AWS CloudFormation",
            "participant S3 as S3 Buckets",
        ]
        if has_emr:
            participants.append("participant EMR as AWS EMR")

        lines.extend(participants)

        lines.append("Op->>TF: terraform apply")
        lines.append("activate TF")

        # Upload CF template to S3
        lines.append("TF->>S3: Upload CF template to S3")
        lines.append("S3-->>TF: Template URL")

        # Create stack
        # Use real stack names from data if available
        stack_names = []
        for _, repo in cf_repos:
            for res in repo["terraform_resources"]:
                if "cloudformation" in res["type"]:
                    stack_names.append(res["name"])
        stack_label = stack_names[0] if stack_names else "EMR"

        lines.append(f"TF->>CF: Create/Update Stack ({stack_label} template)")
        lines.append("activate CF")
        lines.append("Note over CF: CloudFormation processes template")

        if has_emr:
            lines.append("CF->>EMR: Create EMR Cluster")
            lines.append("activate EMR")
        lines.append("CF->>S3: Create logging bucket")

        # Polling loop
        lines.append("loop Stack in progress")
        lines.append("TF->>CF: Describe Stack Status")
        lines.append("CF-->>TF: CREATE_IN_PROGRESS")
        lines.append("end")

        if has_emr:
            lines.append("EMR-->>CF: Cluster ready")
            lines.append("deactivate EMR")

        lines.append("CF-->>TF: CREATE_COMPLETE + Outputs")
        lines.append("deactivate CF")

        lines.append("Note over TF: Extract CF outputs<br/>(cluster ID, DNS, etc.)")
        lines.append("TF->>S3: Configure bucket policies<br/>using CF outputs")
        lines.append("TF-->>Op: Apply complete with outputs")
        lines.append("deactivate TF")

        return (
            "Terraform-CloudFormation Integration",
            "Hybrid IaC flow where Terraform orchestrates CloudFormation stack deployment.",
            lines,
        )

    def _build_runtime_sequence_flow(self) -> tuple | None:
        """Build a runtime/operational sequence for EMR job submission."""
        # Only generate if EMR resources exist
        has_emr = any(
            "Compute/EMR" in r["resource_categories"]
            for r in self._parsed_repos
        )
        if not has_emr:
            return None

        lines = []
        participants = [
            "participant User as Data Engineer",
            "participant CI as CI/CD Pipeline",
            "participant S3 as S3 (Scripts/Data)",
            "participant EMR as EMR Master Node",
            "participant YARN as YARN ResourceManager",
            "participant Workers as EMR Core/Task Nodes",
        ]

