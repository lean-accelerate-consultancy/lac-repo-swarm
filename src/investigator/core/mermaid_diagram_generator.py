    def _generate_infrastructure_diagram(self) -> str:
        """Diagram 3: Layered infrastructure architecture from Terraform resources."""
        # Collect all resources across repos
        all_resources: list[dict] = []
        all_categories: dict[str, int] = defaultdict(int)
        provisioning_repos: list[tuple[int, dict]] = []

        for i, repo in enumerate(self._parsed_repos):
            if repo["terraform_resources"]:
                provisioning_repos.append((i, repo))
                all_resources.extend(repo["terraform_resources"])
            for cat, count in repo["resource_categories"].items():
                all_categories[cat] += count

        if not all_resources:
            return ""

        # Group resources by architectural layer
        layers: dict[str, list[dict]] = defaultdict(list)
        for res in all_resources:
            layer = _LAYER_MAP.get(res["category"], "other")
            layers[layer].append(res)

        lines = ["```mermaid", "---", "title: Infrastructure Architecture — Layered Resource View", "---", "graph TD"]

        # Add repo nodes at the top
        if provisioning_repos:
            lines.append('    subgraph IaC["Infrastructure as Code"]')
            for idx, repo in provisioning_repos:
                node_id = self._safe_id(repo["display_name"], idx)
                lines.append(f'        {node_id}["{repo["display_name"]}<br/>{repo["primary_language"]}"]')
            lines.append("    end")

