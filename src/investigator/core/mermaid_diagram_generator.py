    def _get_style_definitions(self) -> list[str]:
        """Return classDef lines for diagram styling."""
        return [
            "classDef iacNode fill:#e6ccff,stroke:#6600cc,stroke-width:2px",
            "classDef serviceNode fill:#ccffcc,stroke:#006600,stroke-width:2px",
            "classDef dataNode fill:#cce5ff,stroke:#0066cc,stroke-width:2px",
            "classDef securityNode fill:#ffffcc,stroke:#cccc00,stroke-width:2px",
            "classDef computeNode fill:#ffe6cc,stroke:#cc6600,stroke-width:2px",
            "classDef networkNode fill:#e6f3ff,stroke:#3399cc,stroke-width:2px",
        ]

    def _classify_repo(self, repo_data: dict) -> str:
        """Classify a repo by its primary role."""
        lang = repo_data["primary_language"].lower()
        if lang in ("terraform", "cloudformation", "pulumi") or repo_data["module_chain"]:
            return "iac"
        if repo_data["api_endpoints"]:
            return "service"
        if repo_data["event_brokers"] or repo_data["event_topics"]:
            return "event_processor"
        if repo_data["databases"]:
            return "data"
        return "library"

    def _node_shape(self, node_id: str, label: str, category: str) -> str:
        """Return Mermaid node with appropriate shape for the category."""
        if category == "storage":
            return f'{node_id}[("{label}")]'
        if category == "compute":
            return f'{node_id}{{{{"{label}"}}}}'
        if category == "service":
            return f'{node_id}("{label}")'
        if category == "security":
            return f'{node_id}{{{{"{label}"}}}}'
        if category == "broker":
            return f'{node_id}(["{label}"])'
        if category == "user":
            return f'{node_id}(("{label}"))'
        return f'{node_id}["{label}"]'

    def _infer_repo_connections(self) -> list[tuple]:
        """Infer cross-repo connections from shared data.

        Returns list of (from_idx, to_idx, label, style) where style is
        "solid" or "dotted".
        """
        connections = []
        seen = set()  # (from_idx, to_idx, connection_type) to allow multiple edge types

        # Shared module sources (e.g. both repos use ./vpc module)
        source_to_repos: dict[str, list[int]] = defaultdict(list)
        for i, repo in enumerate(self._parsed_repos):
            for mod in repo["module_chain"]:
                source_to_repos[mod["source"]].append(i)
        for source, indices in source_to_repos.items():
            if len(indices) > 1:
                mod_name = source.lstrip("./")
                for j in range(1, len(indices)):
                    key = (indices[0], indices[j], "module")
                    if key not in seen:
                        seen.add(key)
                        connections.append(
                            (indices[0], indices[j], f"shared {mod_name} module", "dotted")
                        )

        # Shared resource categories
        cat_to_repos: dict[str, list[int]] = defaultdict(list)
        for i, repo in enumerate(self._parsed_repos):
            for cat in repo["resource_categories"]:
                cat_to_repos[cat].append(i)
        for cat, indices in cat_to_repos.items():
            if len(indices) > 1:
                for j in range(1, len(indices)):
                    key = (indices[0], indices[j], "resource")
                    if key not in seen:
                        seen.add(key)
                        connections.append(
                            (indices[0], indices[j], f"shared {cat} resources", "dotted")
                        )

        # Shared event topics (cross-repo pub/sub)
        topic_to_repos: dict[str, list[int]] = defaultdict(list)
        for i, repo in enumerate(self._parsed_repos):
            for topic in repo["event_topics"]:
                topic_to_repos[topic["name"]].append(i)
        for topic, indices in topic_to_repos.items():
            if len(indices) > 1:
                key = (indices[0], indices[1], "event")
                if key not in seen:
                    seen.add(key)
                    connections.append(
                        (indices[0], indices[1], f"event: {topic}", "solid")
                    )

        # Shared database types
        db_to_repos: dict[str, list[int]] = defaultdict(list)
