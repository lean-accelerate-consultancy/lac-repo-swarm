class MermaidDiagramGenerator:
    """Generates Mermaid diagrams from static analysis .arch.md content."""

    def __init__(self, repo_contents: dict[str, str]):
        """
        Args:
            repo_contents: dict mapping repo_name -> arch.md content string
        """
        self.repo_contents = repo_contents
        self._parsed_repos: list[dict] = []

        for repo_name, content in repo_contents.items():
            parsed = self._parse_repo_data(repo_name, content)
            if parsed:
                self._parsed_repos.append(parsed)

    def generate_all(self, generated_date: str | None = None) -> str:
        """Generate all diagrams, return as combined markdown document.

        Args:
            generated_date: Optional date string for the header. If None, uses today.
        """
        if not self._parsed_repos:
            return self._empty_document(generated_date)

        date_str = generated_date or datetime.now().strftime("%Y-%m-%d")

        sections = [
            "# Cross-Repository Architecture Overview",
            "",
            "This document was automatically generated by RepoSwarm's static analysis.",
            f"Generated on: {date_str}",
            "",
            "---",
            "",
        ]

        # Diagram 0: Cross-Repo Lifecycle (overarching view of all repos)
        lifecycle = self._generate_cross_repo_lifecycle_diagram()
        if lifecycle:
            sections.append("## Cross-Repository Lifecycle")
            sections.append("")
            sections.append(lifecycle)
            sections.append("")

        # Diagram 1: Repository Overview (enriched with connections)
        overview = self._generate_overview_diagram()
        if overview:
            sections.append("## Network/Architecture Diagram")
            sections.append("")
            sections.append(overview)
            sections.append("")

        # Key Connections narrative (after overview)
        key_connections = self._generate_key_connections_narrative()
        if key_connections:
            sections.append(key_connections)
            sections.append("")

        # Diagram 2: Technology Stack
        tech_stack = self._generate_tech_stack_diagram()
        if tech_stack:
            sections.append("## Technology Stack")
            sections.append("")
            sections.append(tech_stack)
            sections.append("")

        # Diagram 3: Infrastructure Architecture
        infra = self._generate_infrastructure_diagram()
        if infra:
            sections.append("## Infrastructure Architecture")
            sections.append("")
            sections.append(infra)
            sections.append("")

        # Diagram 4: Data Flow
        data_flow = self._generate_data_flow_diagram()
        if data_flow:
            sections.append("## Data Flow")
            sections.append("")
            sections.append(data_flow)
            sections.append("")

            # Data Flow Summary (after data flow diagram)
            summary = self._generate_data_flow_summary()
            if summary:
                sections.append(summary)
                sections.append("")

            # Data Governance Concerns
            governance = self._generate_governance_concerns()
            if governance:
                sections.append(governance)
                sections.append("")

        # Diagram 5: Module Structure (per-repo)
        module_diagrams = self._generate_module_diagrams()
        if module_diagrams:
            sections.append("## Module Structure")
            sections.append("")
            sections.append(module_diagrams)
            sections.append("")

        # Diagram 6: Language Distribution
        pie_chart = self._generate_language_pie_chart()
        if pie_chart:
            sections.append("## Language Distribution")
            sections.append("")
            sections.append(pie_chart)
            sections.append("")

        # Diagram 7+: Sequence Diagrams (enriched)
        sequence = self._generate_sequence_diagrams()
        if sequence:
            sections.append("## Service Interaction Sequences")
            sections.append("")
            sections.append(sequence)
            sections.append("")

        sections.append("---")
        sections.append("*Generated by static analysis (ENABLE_AI=false)*")

        return "\n".join(sections)

    # ------------------------------------------------------------------
    # Parsing
    # ------------------------------------------------------------------

    def _parse_repo_data(self, repo_name: str, content: str) -> dict | None:
        """Extract structured data from an .arch.md file."""
        if not content or not content.strip():
            return None

        data = {
            "repo_name": repo_name,
            "display_name": repo_name,
            "primary_language": "Unknown",
            "total_loc": 0,
            "total_files": 0,
            "tech_stack": [],
            "languages": {},  # {lang: loc}
            "modules": [],    # [{name, files, subdirs}]
            "api_endpoints": [],  # [{method, path, detail}]
            "event_brokers": [],  # ["Kafka", "RabbitMQ", ...]
            "event_topics": [],   # [{name, type}]
            "databases": [],      # ["PostgreSQL", "DynamoDB", ...]
            "module_chain": [],   # [{name, source}]
            "outputs": [],        # [{name, file}]
            "terraform_resources": [],  # [{type, name, category, file}]
            "resource_categories": {},  # {category: count}
            "iam_resources": [],        # [{type, name, file}]
            "security_resources": [],   # [{type, name, category, file}]
            "resource_groups": [],      # [{group, resources, files}]
            "providers": [],            # ["aws", "google", ...]
        }

        # Extract repo name from content (may differ from filename key)
        name_match = re.search(
            r'\*\*Repository Name:\*\*\s*(.+)', content
        )
        if name_match:
            data["display_name"] = name_match.group(1).strip()

        # Primary language
        lang_match = re.search(
            r'\*\*Primary Language:\*\*\s*(.+)', content
        )
        if lang_match:
            data["primary_language"] = lang_match.group(1).strip()

        # Total LOC
        loc_match = re.search(
            r'\*\*Total Lines of Code:\*\*\s*([\d,]+)', content
        )
        if loc_match:
            data["total_loc"] = int(loc_match.group(1).replace(",", ""))

        # Total files
        files_match = re.search(
            r'\*\*Total Files:\*\*\s*([\d,]+)', content
        )
        if files_match:
            data["total_files"] = int(files_match.group(1).replace(",", ""))

        # Tech stack (lines starting with "- " after **Technology Stack:**)
        tech_match = re.search(
            r'\*\*Technology Stack:\*\*\s*\n((?:\s*-\s*.+\n?)+)', content
        )
        if tech_match:
            tech_block = tech_match.group(1)
            data["tech_stack"] = [
                line.strip().lstrip("- ").strip()
                for line in tech_block.strip().split("\n")
                if line.strip().startswith("-")
            ]

        # Language breakdown table
        # | Language | Files | Lines of Code | Blank | Comment |
        lang_table_match = re.search(
            r'\*\*Language Breakdown:\*\*\s*\n'
            r'\s*\|[^\n]*Language[^\n]*\|\s*\n'  # header
            r'\s*\|[-| :]+\|\s*\n'                # separator
            r'((?:\s*\|[^\n]+\|\s*\n?)+)',          # rows
            content,
        )
        if lang_table_match:
            rows_text = lang_table_match.group(1)
            for row in rows_text.strip().split("\n"):
                cells = [c.strip() for c in row.strip().strip("|").split("|")]
                if len(cells) >= 3:
                    lang = cells[0].strip()
                    try:
                        loc = int(cells[2].strip().replace(",", ""))
                        if lang and loc >= 0:
                            data["languages"][lang] = loc
                    except (ValueError, IndexError):
                        pass

        # Module/directory breakdown table
        # | Module | Files | Subdirectories |
        module_match = re.search(
            r'\*\*Module/Directory Breakdown:\*\*\s*\n'
            r'\s*\|[^\n]*Module[^\n]*\|\s*\n'     # header
            r'\s*\|[-| :]+\|\s*\n'                  # separator
            r'((?:\s*\|[^\n]+\|\s*\n?)+)',            # rows
            content,
        )
        if module_match:
            rows_text = module_match.group(1)
            for row in rows_text.strip().split("\n"):
                cells = [c.strip() for c in row.strip().strip("|").split("|")]
                if len(cells) >= 2:
                    mod_name = cells[0].strip().strip("`").rstrip("/")
                    try:
                        files_count = int(cells[1].strip())
                    except (ValueError, IndexError):
                        files_count = 0
                    try:
                        subdirs = int(cells[2].strip()) if len(cells) >= 3 else 0
                    except (ValueError, IndexError):
                        subdirs = 0
                    if mod_name:
                        data["modules"].append({
                            "name": mod_name,
                            "files": files_count,
                            "subdirs": subdirs,
                        })

        # --- Parse APIs section ---
        apis_section = self._extract_section(content, "APIs")
        if apis_section and "*No " not in apis_section[:20]:
            # Match: | METHOD | `path` | detail |
            api_re = re.compile(
                r'\|\s*(GET|POST|PUT|DELETE|PATCH|HEAD|OPTIONS|ANY)\s*\|'
                r'\s*`?([^|`]+)`?\s*\|'
                r'\s*([^|]*)\|',
                re.IGNORECASE,
            )
            for m in api_re.finditer(apis_section):
                data["api_endpoints"].append({
                    "method": m.group(1).upper().strip(),
                    "path": m.group(2).strip(),
                    "detail": m.group(3).strip(),
                })

        # --- Parse events section ---
        events_section = self._extract_section(content, "events")
        if events_section and "*No " not in events_section[:20]:
            # Broker names from bullet list: - **Kafka**
            broker_re = re.compile(r'^\s*-\s*\*\*([^*]+)\*\*', re.MULTILINE)
            for m in broker_re.finditer(events_section):
                broker = m.group(1).strip()
                if broker not in data["event_brokers"]:
                    data["event_brokers"].append(broker)

            # Topics from table: | `topic.name` | type | file |
            # Only parse after **Topics/Queues:** header
            topics_block = ""
            topics_header = re.search(r'\*\*Topics/Queues:\*\*', events_section)
            if topics_header:
                topics_block = events_section[topics_header.end():]
                # Stop at next header
                next_header = re.search(r'\n\*\*[A-Z]', topics_block)
                if next_header:
                    topics_block = topics_block[:next_header.start()]

            if topics_block:
                topic_re = re.compile(r'\|\s*`([^`]+)`\s*\|\s*([^|]+)\|')
                for m in topic_re.finditer(topics_block):
                    data["event_topics"].append({
                        "name": m.group(1).strip(),
                        "type": m.group(2).strip(),
                    })

        # --- Parse DBs section ---
        dbs_section = self._extract_section(content, "DBs")
        if dbs_section and "*No " not in dbs_section[:20]:
            db_types = set()
            db_row_re = re.compile(
                r'^\s*\|\s*`?([A-Z][\w\s/]+[\w])`?\s*\|',
                re.MULTILINE,
            )
            for m in db_row_re.finditer(dbs_section):
                db_type = m.group(1).strip().strip("`")
                if db_type.lower() not in ("type", "name", "database", "database type",
                                           "resource type", "schema type"):
                    db_types.add(db_type)

            data["databases"] = sorted(db_types)

        # --- Parse service_dependencies section ---
        svc_section = self._extract_section(content, "service_dependencies")
        if svc_section and "*No " not in svc_section[:20]:
            # Module chain: root -> module_name (source: ./path)
            chain_re = re.compile(r'root\s*->\s*(\S+)\s*\(source:\s*([^)]+)\)')
            for m in chain_re.finditer(svc_section):
                data["module_chain"].append({
                    "name": m.group(1).strip(),
                    "source": m.group(2).strip(),
                })

            # Outputs: - `name` (file)
            output_re = re.compile(r'-\s*`([^`]+)`\s*\(([^)]+)\)')
            outputs_header = re.search(r'\*\*Outputs Exposed:\*\*', svc_section)
            if outputs_header:
                outputs_block = svc_section[outputs_header.end():]
                for m in output_re.finditer(outputs_block):
                    data["outputs"].append({
                        "name": m.group(1).strip(),
                        "file": m.group(2).strip(),
                    })

            # Providers: | `aws` | main.tf |
            provider_re = re.compile(r'\|\s*`([^`]+)`\s*\|', re.MULTILINE)
            providers_header = re.search(r'\*\*Providers:\*\*', svc_section)
            if providers_header:
                providers_block = svc_section[providers_header.end():]
                next_header = re.search(r'\n\*\*[A-Z]', providers_block)
                if next_header:
                    providers_block = providers_block[:next_header.start()]
                for m in provider_re.finditer(providers_block):
                    prov = m.group(1).strip()
                    if prov.lower() not in ("provider", "module", "source"):
                        data["providers"].append(prov)

        # --- Parse resources section ---
        self._parse_resources_section(content, data)

        # --- Parse authentication section ---
        self._parse_authentication_section(content, data)

        # --- Parse authorization section ---
        self._parse_authorization_section(content, data)

        # --- Parse core_entities section ---
        self._parse_core_entities_section(content, data)

        return data

    def _parse_resources_section(self, content: str, data: dict) -> None:
        """Parse the # resources section for Terraform infrastructure resources."""
        resources_section = self._extract_section(content, "resources")
        if not resources_section or "*No " in resources_section[:20]:
            return

        # Individual resources: | `aws_vpc` | main | Networking | main.tf |
        res_re = re.compile(
            r'\|\s*`([^`]+)`\s*\|\s*([^|]+)\|\s*([^|]+)\|\s*([^|]+)\|'
        )
        for m in res_re.finditer(resources_section):
            res_type = m.group(1).strip()
            res_name = m.group(2).strip()
            category = m.group(3).strip()
            file_path = m.group(4).strip()
            # Skip header rows
            if res_type.lower() in ("resource type",):
                continue
            data["terraform_resources"].append({
                "type": res_type,
                "name": res_name,
                "category": category,
                "file": file_path,
            })

        # Category summary: | Networking | 18 |
        cat_header = re.search(r'\*\*Resources by Category:\*\*', resources_section)
        if cat_header:
            cat_block = resources_section[cat_header.end():]
            next_header = re.search(r'\n\*\*[A-Z]', cat_block)
            if next_header:
                cat_block = cat_block[:next_header.start()]
            cat_re = re.compile(r'\|\s*([A-Za-z][^|]+?)\s*\|\s*(\d+)\s*\|')
            for m in cat_re.finditer(cat_block):
                cat_name = m.group(1).strip()
                if cat_name.lower() not in ("category", "count"):
                    try:
                        data["resource_categories"][cat_name] = int(m.group(2))
                    except ValueError:
                        pass

    def _parse_authentication_section(self, content: str, data: dict) -> None:
        """Parse the # authentication section for IAM resources."""
        auth_section = self._extract_section(content, "authentication")
        if not auth_section or "*No " in auth_section[:20]:
            return

        # | `aws_iam_role` | emr_iam_role | emr/emr.tf |
        iam_re = re.compile(
            r'\|\s*`([^`]+)`\s*\|\s*([^|]+)\|\s*([^|]+)\|'
        )
        for m in iam_re.finditer(auth_section):
            res_type = m.group(1).strip()
            res_name = m.group(2).strip()
            file_path = m.group(3).strip()
            if res_type.lower() in ("resource type",):
                continue
            data["iam_resources"].append({
                "type": res_type,
                "name": res_name,
                "file": file_path,
            })

    def _parse_authorization_section(self, content: str, data: dict) -> None:
        """Parse the # authorization section for security resources."""
        authz_section = self._extract_section(content, "authorization")
        if not authz_section or "*No " in authz_section[:20]:
            return

        # | `aws_security_group` | sg_1 | Security | vpc/vpc.tf |
        sec_re = re.compile(
            r'\|\s*`([^`]+)`\s*\|\s*([^|]+)\|\s*([^|]+)\|\s*([^|]+)\|'
        )
        for m in sec_re.finditer(authz_section):
            res_type = m.group(1).strip()
            res_name = m.group(2).strip()
            category = m.group(3).strip()
            file_path = m.group(4).strip()
            if res_type.lower() in ("resource type",):
                continue
            data["security_resources"].append({
                "type": res_type,
                "name": res_name,
                "category": category,
                "file": file_path,
            })

    def _parse_core_entities_section(self, content: str, data: dict) -> None:
        """Parse the # core_entities section for Terraform resource groups."""
        entities_section = self._extract_section(content, "core_entities")
        if not entities_section or "*No " in entities_section[:20]:
            return

        # | Vpc | 7 | vpc/vpc.tf, bastion/bastion.tf |
        group_re = re.compile(
            r'\|\s*([A-Z][a-zA-Z_]+)\s*\|\s*(\d+)\s*\|\s*([^|]+)\|'
        )
        for m in group_re.finditer(entities_section):
            group_name = m.group(1).strip()
            if group_name.lower() in ("group", "resources"):
                continue
            try:
                count = int(m.group(2))
            except ValueError:
                count = 0
            data["resource_groups"].append({
                "group": group_name,
                "resources": count,
                "files": m.group(3).strip(),
            })

