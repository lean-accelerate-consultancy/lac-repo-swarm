    def _generate_key_connections_narrative(self) -> str:
        """Generate narrative text about key architectural connections."""
        all_categories = set()
        has_cf = False
        has_s3 = False
        for repo in self._parsed_repos:
            all_categories.update(repo["resource_categories"].keys())
            for res in repo["terraform_resources"]:
                if "cloudformation" in res["type"]:
                    has_cf = True
                if res["type"] == "aws_vpc_endpoint" and "s3" in res["name"].lower():
                    has_s3 = True
        if "Storage/S3" in all_categories:
            has_s3 = True

        if not all_categories:
            return ""

        items = []
        count = 1

        # EMR as central target
        if "Compute/EMR" in all_categories:
            emr_repos = [r["display_name"] for r in self._parsed_repos if "Compute/EMR" in r["resource_categories"]]
            items.append(
                f"### {count}. **AWS EMR as Central Target Resource**\n"
                f"Repositories converge on provisioning AWS EMR clusters. "
                f"This represents the core purpose of this infrastructure — managing big data processing. "
                f"Repos involved: {', '.join(f'`{n}`' for n in emr_repos)}."
            )
            count += 1

        # Shared VPC
        vpc_repos = [r["display_name"] for r in self._parsed_repos if "Networking" in r["resource_categories"]]
        if len(vpc_repos) > 1:
            items.append(
                f"### {count}. **VPC/Network Foundation (Shared Concern)**\n"
                f"Multiple repositories handle VPC provisioning ({', '.join(f'`{n}`' for n in vpc_repos)}), "
                f"suggesting either different approaches to the same problem or a shared networking module pattern."
            )
            count += 1

        # TF-CF bridge
        if has_cf:
            items.append(
                f"### {count}. **Terraform-CloudFormation Bridge**\n"
                f"A hybrid IaC approach allows CloudFormation stacks to be managed within Terraform workflows. "
                f"This is a critical integration point for leveraging CloudFormation-specific features."
            )
            count += 1

        # S3 shared data layer
        if has_s3:
            items.append(
                f"### {count}. **S3 as Shared Data Layer**\n"
                f"EMR clusters require S3 for input data, output results, and logs. "
                f"S3 bucket configuration and access policies are a potential single point of failure."
            )
            count += 1

        # IAM security backbone
        if "IAM" in all_categories:
            items.append(
                f"### {count}. **IAM as Security Backbone**\n"
                f"All deployments require IAM roles and policies. "
                f"This is a critical security boundary if role permissions are incorrectly scoped."
            )
            count += 1

        if not items:
            return ""

        return "## Key Connections\n\n" + "\n\n".join(items)

