        result = gen._generate_overview_diagram()
        lines = result.split("\n")
        # Find the title line
        title_idx = None
        for i, line in enumerate(lines):
            if "title:" in line:
                title_idx = i
                break
        self.assertIsNotNone(title_idx, "Should have a title line")
        # Title should be between --- markers
        self.assertEqual(lines[title_idx - 1], "---")
        self.assertEqual(lines[title_idx + 1], "---")


