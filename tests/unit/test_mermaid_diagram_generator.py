class TestMermaidDiagramGeneratorParsing(unittest.TestCase):
    """Tests for the .arch.md parsing logic."""

    def test_parse_single_repo(self):
        """Single repo content is parsed correctly."""
        gen = MermaidDiagramGenerator({"repo1": SAMPLE_ARCH_CONTENT_1})
        self.assertEqual(len(gen._parsed_repos), 1)
        repo = gen._parsed_repos[0]
        self.assertEqual(repo["display_name"], "aws-terraform-emr-network")
        self.assertEqual(repo["primary_language"], "Terraform")
        self.assertEqual(repo["total_loc"], 627)
        self.assertEqual(repo["total_files"], 18)

    def test_parse_tech_stack(self):
        """Tech stack list is extracted."""
        gen = MermaidDiagramGenerator({"repo1": SAMPLE_ARCH_CONTENT_1})
        repo = gen._parsed_repos[0]
        self.assertEqual(repo["tech_stack"], ["Terraform", "Docker"])

    def test_parse_language_breakdown(self):
        """Language breakdown table is parsed into lang->LOC dict."""
        gen = MermaidDiagramGenerator({"repo1": SAMPLE_ARCH_CONTENT_1})
        repo = gen._parsed_repos[0]
        self.assertEqual(repo["languages"]["Terraform"], 626)
        self.assertEqual(repo["languages"]["Shell"], 1)

    def test_parse_modules(self):
        """Module breakdown table is parsed."""
        gen = MermaidDiagramGenerator({"repo1": SAMPLE_ARCH_CONTENT_1})
        repo = gen._parsed_repos[0]
        self.assertEqual(len(repo["modules"]), 4)
        mod_names = {m["name"] for m in repo["modules"]}
        self.assertEqual(mod_names, {"arch-docs", "bastion", "emr", "vpc"})
        emr = next(m for m in repo["modules"] if m["name"] == "emr")
        self.assertEqual(emr["files"], 5)

    def test_parse_multiple_repos(self):
        """Multiple repos are all parsed."""
        gen = MermaidDiagramGenerator({
            "repo1": SAMPLE_ARCH_CONTENT_1,
            "repo2": SAMPLE_ARCH_CONTENT_2,
        })
        self.assertEqual(len(gen._parsed_repos), 2)

    def test_empty_content_skipped(self):
        """Empty content is skipped."""
        gen = MermaidDiagramGenerator({"repo1": "", "repo2": SAMPLE_ARCH_CONTENT_1})
        self.assertEqual(len(gen._parsed_repos), 1)

    def test_empty_dict(self):
        """Empty dict produces no parsed repos."""
        gen = MermaidDiagramGenerator({})
        self.assertEqual(gen._parsed_repos, [])


