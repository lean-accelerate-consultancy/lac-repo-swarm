class TestStaticAnalyzerInit(unittest.TestCase):
    """Tests for StaticAnalyzer initialization."""

    def test_creates_with_repo_path(self):
        """StaticAnalyzer can be instantiated with a repo path."""
        analyzer = StaticAnalyzer(str(SAMPLE_REPO))
        self.assertEqual(analyzer.repo_path, str(SAMPLE_REPO))

    def test_creates_with_custom_logger(self):
        """StaticAnalyzer accepts a custom logger."""
        logger = MagicMock()
        analyzer = StaticAnalyzer(str(SAMPLE_REPO), logger=logger)
        self.assertEqual(analyzer.logger, logger)

    def test_section_dispatch_table_has_19_entries(self):
        """The dispatch table should map all 19 analysis sections (17 original + resources + environments)."""
        analyzer = StaticAnalyzer(str(SAMPLE_REPO))
        self.assertEqual(len(analyzer._section_analyzers), 19)


