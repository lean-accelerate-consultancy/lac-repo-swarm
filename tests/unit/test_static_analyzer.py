class TestStaticAnalyzerWithTempRepo(unittest.TestCase):
    """Tests using a temporary repo to control exact file contents."""

    def setUp(self):
        self.tmpdir = tempfile.mkdtemp()
        self.analyzer = StaticAnalyzer(self.tmpdir)

    def tearDown(self):
        import shutil
        shutil.rmtree(self.tmpdir, ignore_errors=True)

    def _write_file(self, name, content):
        filepath = Path(self.tmpdir) / name
        filepath.parent.mkdir(parents=True, exist_ok=True)
        filepath.write_text(content, encoding="utf-8")

    def test_hl_overview_no_readme(self):
        """hl_overview works when there is no README file."""
        self._write_file("main.py", "print('hello')")
        result = self.analyzer.analyze_section("hl_overview", "main.py")
        self.assertIsInstance(result, str)
        self.assertNotIn("**Project Purpose:**", result)

    def test_hl_overview_long_structure_truncated(self):
        """hl_overview truncates very long repo structures."""
        long_structure = "\n".join([f"file_{i}.py" for i in range(200)])
        self._write_file("main.py", "x = 1")
        result = self.analyzer.analyze_section("hl_overview", long_structure)
        self.assertIn("more lines", result)

    def test_module_deep_dive_empty_repo(self):
        """module_deep_dive handles a repo with no subdirectories."""
        self._write_file("main.py", "x = 1")
        result = self.analyzer.analyze_section("module_deep_dive", "main.py")
        self.assertIn("No significant module structure detected", result)

    def test_dependencies_no_dep_files(self):
        """dependencies handles a repo with no standard dependency files."""
        self._write_file("main.py", "x = 1")
        result = self.analyzer.analyze_section("dependencies", "main.py", deps_data=None)
        self.assertIn("No standard dependency files detected", result)


class TestStaticAnalyzerErrorHandling(unittest.TestCase):
    """Tests for error handling in analyze_section."""

    def test_analyzer_error_returns_error_section(self):
        """If a section analyzer raises, an error section is returned instead of crashing."""
        analyzer = StaticAnalyzer(str(SAMPLE_REPO))

        # Monkey-patch an analyzer to raise
        def _bad_analyzer(*args):
            raise ValueError("test explosion")

        analyzer._section_analyzers["hl_overview"] = _bad_analyzer

        result = analyzer.analyze_section("hl_overview", SAMPLE_STRUCTURE)
        self.assertIn("encountered an error", result)
        self.assertIn("test explosion", result)


